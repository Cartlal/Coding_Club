/**
 * Filter events by status
 * @param {string} status - 'upcoming', 'ongoing', 'completed'
 * @returns {Array} Filtered events
 */
export function getEventsByStatus(status) {
  return mockEvents.filter((event) => event.status === status);
}

/**
 * Filter events by category
 * @param {string} category - Event category
 * @returns {Array} Filtered events
 */
export function getEventsByCategory(category) {
  return mockEvents.filter((event) => event.category === category);
}

/**
 * Get event by ID
 * @param {number} id - Event ID
 * @returns {Object} Event object or null
 */
export function getEventById(id) {
  return mockEvents.find((event) => event.id === id) || null;
}

/**
 * Get all unique categories
 * @returns {Array} Array of unique categories
 */
export function getAllCategories() {
  const categories = mockEvents.map((event) => event.category);
  return [...new Set(categories)];
}
